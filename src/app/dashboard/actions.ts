"use server";

import path from "path";
import fs from "fs/promises";
import { randomUUID } from "crypto";
import ffmpeg from "@/lib/ffmpeg";
import sharp from "sharp";
import { exiftool } from "exiftool-vendored";

/* ------------ Réglages généraux ------------- */
const OUT_DIR = path.join(process.cwd(), "public", "out");
async function ensureOut() {
  await fs.mkdir(OUT_DIR, { recursive: true });
}

/* ------------ Helpers type fichier ------------- */
function isVideo(mime?: string | null) {
  return !!mime && mime.startsWith("video/");
}
function isImage(mime?: string | null) {
  // on cible jpg/png/webp ; sharp gère très bien ces formats
  return !!mime && (mime.startsWith("image/"));
}

/* ------------ Écrire un File (Web API) sur le disque ------------- */
async function writeFileFromWebFile(file: File, fullPath: string) {
  const arrayBuffer = await file.arrayBuffer();
  await fs.writeFile(fullPath, Buffer.from(arrayBuffer));
}

/* ============================================
 *               VIDÉO — variations
 * ============================================ */
function rnd(min: number, max: number) {
  return min + Math.random() * (max - min);
}
function coin(p = 0.5) {
  return Math.random() < p;
}

function randomVideoParams(i: number) {
  const brightness = +rnd(-0.03, 0.03).toFixed(3);
  const contrast   = +rnd(0.97, 1.03).toFixed(3);
  const sat        = +rnd(0.97, 1.03).toFixed(3);
  const hue        = +rnd(-0.03, 0.03).toFixed(3);
  const noiseLevel = Math.floor(rnd(1, 5));
  const shiftX = coin() ? 1 : 0;
  const shiftY = shiftX ? 0 : 1;
  const speed = +rnd(0.985, 1.015).toFixed(3);
  const volDb = +rnd(-1.0, 1.0).toFixed(2);

  const vfParts: string[] = [
    "scale=trunc(iw/2)*2:trunc(ih/2)*2",
    `crop=in_w-${shiftX?1:0}:in_h-${shiftY?1:0}:${shiftX?1:0}:${shiftY?1:0},pad=iw+${shiftX?1:0}:ih+${shiftY?1:0}:${shiftX?1:0}:${shiftY?1:0}:color=black`,
    `noise=alls=${noiseLevel}:allf=t+u`,
    `eq=brightness=${brightness}:contrast=${contrast}:saturation=${sat}`,
    `hue=h=${hue}*PI:s=${sat}`
  ];
  if (coin(0.4)) vfParts.push("unsharp=lx=3:ly=3:la=0.8:cx=3:cy=3:ca=0.8");

  const setpts = +(1 / speed).toFixed(6);
  const vf = `${vfParts.join(",")},setpts=${setpts}*PTS`;
  const af = [`volume=${volDb}dB`, `atempo=${speed}`].join(",");

  const metaTitle = `Duplicate_${i}`;
  const metaComment = `Generated by ContentDuplicator (n=${i}, spd=${speed}, hue=${hue}, sat=${sat}, br=${brightness}, ct=${contrast}, nz=${noiseLevel})`;

  return { vf, af, metaTitle, metaComment };
}

/* ============================================
 *            IMAGE — variations & EXIF
 * ============================================ */
/**
 * Objectif (par duplication) :
 *  - Nom différent (déjà fait par le fichier de sortie)
 *  - Taille du fichier différente (qualité JPEG aléatoire)
 *  - Height / Width légèrement différents (scale 0.992–1.008)
 *  - EXIF/metadata modifiés (Title/Comment/Software + Date)
 *  - Légère variation visuelle (brightness/saturation, gamma)
 */
async function duplicateImageOnce(
  inputPath: string,
  i: number
): Promise<{ outName: string; outPath: string }> {
  const meta = await sharp(inputPath).metadata();
  const baseW = meta.width || 0;
  const baseH = meta.height || 0;

  // micro redimensionnement pour changer W/H
  const scale = +rnd(0.992, 1.008).toFixed(4);
  const newW = Math.max(16, Math.round(baseW * scale));
  const newH = Math.max(16, Math.round(baseH * scale));

  // variations visuelles légères
  const brightness = +rnd(0.97, 1.03).toFixed(3);
  const saturation = +rnd(0.97, 1.03).toFixed(3);
  const gamma      = +rnd(0.98, 1.02).toFixed(3);

  // qualité de recompression
  const quality = Math.round(rnd(84, 92)); // 84–92

  const outName = `dup_${i}_${randomUUID()}.jpg`;
  const outPath = path.join(OUT_DIR, outName);

  // pipeline sharp
  await sharp(inputPath)
    .resize(newW, newH, { fit: "fill" })
    .modulate({ brightness, saturation }) // contraste simulé via gamma/brightness
    .gamma(gamma)
    .jpeg({ quality, mozjpeg: true, chromaSubsampling: "4:2:0" })
    .toFile(outPath);

  // EXIF / IPTC : on écrit des métadonnées simples et distinctes
  const now = new Date();
  const dt =
    `${now.getFullYear()}:${String(now.getMonth()+1).padStart(2,"0")}:` +
    `${String(now.getDate()).padStart(2,"0")} ` +
    `${String(now.getHours()).padStart(2,"0")}:` +
    `${String(now.getMinutes()).padStart(2,"0")}:` +
    `${String(now.getSeconds()).padStart(2,"0")}`;

  try {
    await exiftool.write(outPath, {
      // Quelques champs courants et faciles à vérifier
      AllDates: dt,                   // DateTimeOriginal/Modify/Create
      Software: "ContentDuplicator",
      XPTitle: `Duplicate_${i}`,
      XPComment: `Brightness=${brightness}; Saturation=${saturation}; Gamma=${gamma}; Qual=${quality}; Scale=${scale}`,
      // IPTC de base
      Caption: "Generated by ContentDuplicator",
      // XMP titre/commentaire
      "XMP-dc:Title": `Duplicate_${i}`,
      "XMP-dc:Description": "Generated by ContentDuplicator",
    });
  } catch (e) {
    // On laisse passer si exiftool n'arrive pas à écrire (format exotique)
    console.warn("[exiftool] write warning:", e);
  }

  return { outName, outPath };
}

/* ============================================
 *               ACTIONS EXPORTÉES
 * ============================================ */
export async function deleteAll() {
  try {
    await ensureOut();
    const files = await fs.readdir(OUT_DIR);
    await Promise.all(
      files.map(async (name) => {
        const full = path.join(OUT_DIR, name);
        const stat = await fs.stat(full).catch(() => null);
        if (stat?.isFile()) await fs.unlink(full).catch(() => {});
      })
    );
    return { ok: true };
  } catch (err: any) {
    console.error("[deleteAll] error:", err);
    return { ok: false, error: err?.message || String(err) };
  }
}

export async function duplicate(formData: FormData) {
  await ensureOut();

  const file = formData.get("file") as File | null;
  const countRaw = formData.get("count") as string | null;
  const count = Math.max(1, Math.min(50, Number(countRaw || 1)));

  if (!file) return { ok: false, error: "Aucun fichier fourni." };

  const isVid = isVideo(file.type);
  const isImg = isImage(file.type);

  // Nom temporaire d'entrée (on force .mp4 pour vidéo, .jpg sinon)
  const ext = isVid ? ".mp4" : isImg ? ".jpg" : "";
  const tmpIn = path.join(OUT_DIR, `tmp_${randomUUID()}${ext}`);
  await writeFileFromWebFile(file, tmpIn);

  const outFiles: string[] = [];

  if (isVid) {
    for (let i = 1; i <= count; i++) {
      const outName = `dup_${i}_${randomUUID()}.mp4`;
      const outPath = path.join(OUT_DIR, outName);
      const { vf, af, metaTitle, metaComment } = randomVideoParams(i);

      await new Promise<void>((resolve, reject) => {
        let lastStdErr = "";
        ffmpeg(tmpIn)
          .outputOptions([
            "-c:v libx264",
            "-preset veryfast",
            "-crf 22",
            "-maxrate 4500k",
            "-bufsize 9000k",
            "-movflags +faststart",
            "-c:a aac",
            "-b:a 192k",
            `-vf ${vf}`,
            `-af ${af}`,
            "-metadata", `title=${metaTitle}`,
            "-metadata", `comment=${metaComment}`,
          ])
          .on("stderr", (line) => (lastStdErr = line))
          .on("end", () => resolve())
          .on("error", (err: Error) =>
            reject(new Error(`ffmpeg failed: ${err.message}\n${lastStdErr || ""}`))
          )
          .save(outPath);
      });

      outFiles.push(outName);
    }
  } else if (isImg) {
    // ➜ Images : vraies variations + EXIF
    for (let i = 1; i <= count; i++) {
      const { outName } = await duplicateImageOnce(tmpIn, i);
      outFiles.push(outName);
    }
  } else {
    // Autres types => duplication bête (bytes identiques, juste nom différent)
    const data = await fs.readFile(tmpIn);
    for (let i = 1; i <= count; i++) {
      const outName = `dup_${i}_${randomUUID()}${ext || ""}`;
      await fs.writeFile(path.join(OUT_DIR, outName), data);
      outFiles.push(outName);
    }
  }

  await fs.unlink(tmpIn).catch(() => {});
  return { ok: true, files: outFiles };
}